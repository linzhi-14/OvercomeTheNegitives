PlayerEvents.tick(event => {
    const player = event.player

    if(event.hasGameStage("game_reset")){
        let name = player.name.getString()
        if (event.hasGameStage("all_end") != true){ //全结局下不消阶段
            event.server.runCommandSilent("gamestage clear " + name)
        }
        event.server.runCommandSilent("advancement revoke "+ name +" everything")
        event.server.runCommandSilent("curios reset " + name)
        ftb_quests_id_list.forEach(id => {
            event.server.runCommandSilent("ftbquests change_progress " + name + " reset " + id)
        })
        event.removeGameStage("game_reset")
    }
})